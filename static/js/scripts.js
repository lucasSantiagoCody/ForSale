

function redirect(url)
    {
        location.href=url
    }

function show(element)
    {
        $(element).slideDown(1500)
    }

function hide(element)
    {     
        $(element).slideUp(1500)
    }
function fast_show(element)
    {
        $(element).fadeIn()
    }

function fast_hide(element)
    {     
        $(element).fadeOut()
    }

